namespace Gc\View\Helper;

use Gc\Test\PHPUnit\Framework\TestCase;
use Gc\Document\Model;
use Gc\Registry;
/**
 * Generated by PHPUnit_SkeletonGenerator.
 *
 * @group Gc
 * @category Gc_Tests
 * @package  Library
 */
class CurrentDocumentTest extends TestCase
{
    /**
     * @var Document
     */
    protected $object;

    /**
     * Sets up the fixture, for example, opens a network connection.
     * This method is called before a test is executed.
     *
     * @return void
     */
    protected function setUp()
    {
        $serviceManager = Registry::get('Application')->getServiceManager();
        $renderer       = $serviceManager->get('Zend\View\Renderer\PhpRenderer');
        $serviceManager->setAllowOverride(true);
        $serviceManager->setService(
            'currentDocument',
            Model::fromArray(
                array(
                    'id' => 1,
                )
            )
        );
        $serviceManager->setAllowOverride(false);
        $this->object = new CurrentDocument($serviceManager);
    }

    /**
     * Test
     *
     * @return void
     */
    public function testInvokeFromId()
    {
        $this->assertInstanceOf('Gc\Document\Model', $this->object->__invoke());
    }
}
