namespace Gc\Tab;

use Gc\Test\PHPUnit\Framework\TestCase;
use Gc\DocumentType\Model as DocumentTypeModel;
use Gc\Layout\Model as LayoutModel;
use Gc\User\Model as UserModel;
use Gc\View\Model as ViewModel;

/**
 * Generated by PHPUnit_SkeletonGenerator 1.2.0 on 2012-10-17 at 20:40:10.
 *
 * @group Gc
 * @category Gc_Tests
 * @package  Library
 */
class CollectionTest extends TestCase
{
    /**
     * @var Collection
     */
    protected $object;

    /**
     * @var ViewModel
     */
    protected $view;

    /**
     * @var LayoutModel
     */
    protected $layout;

    /**
     * @var UserModel
     */
    protected $user;

    /**
     * @var DocumentTypeModel
     */
    protected $documentType;

    /**
     * @var Model
     */
    protected $tab;

    /**
     * Sets up the fixture, for example, opens a network connection.
     * This method is called before a test is executed.
     *
     * @return void
     */
    protected function setUp()
    {
        $this->view = ViewModel::fromArray(
            array(
                'name' => 'View Name',
                'identifier' => 'View identifier',
                'description' => 'View Description',
                'content' => 'View Content'
            )
        );
        $this->view->save();

        $this->layout = LayoutModel::fromArray(
            array(
                'name' => 'Layout Name',
                'identifier' => 'Layout identifier',
                'description' => 'Layout Description',
                'content' => 'Layout Content'
            )
        );
        $this->layout->save();

        $this->user = UserModel::fromArray(
            array(
                'lastname' => 'User test',
                'firstname' => 'User test',
                'email' => 'pierre.rambaud86@gmail.com',
                'login' => 'test',
                'user_acl_role_id' => 1,
            )
        );
        $this->user->setPassword('test');
        $this->user->save();

        $this->documentType = DocumentTypeModel::fromArray(
            array(
                'name' => 'Document Type Name',
                'description' => 'Document Type description',
                'icon_id' => 1,
                'defaultview_id' => $this->view->getId(),
                'user_id' => $this->user->getId(),
            )
        );
        $this->documentType->save();

        $this->tab = Model::fromArray(
            array(
                'name' => 'TabTest',
                'description' => 'TabTest',
                'sort_order' => 1,
                'document_type_id' => $this->documentType->getId(),
            )
        );
        $this->tab->save();

        $this->object = new Collection;
    }

    /**
     * Test
     *
     * @return void
     */
    public function testLoad()
    {
        $this->assertInstanceOf('Gc\Tab\Collection', $this->object->load(1));
    }

    /**
     * Test
     *
     * @return void
     */
    public function testGetTabsWithDocumentTypeId()
    {
        $this->object->load(1);
        $this->assertInternalType('array', $this->object->getAll());
    }

    /**
     * Test
     *
     * @return void
     */
    public function testGetTabs()
    {
        $this->assertInternalType('array', $this->object->getAll());
    }

    /**
     * Test
     *
     * @return void
     */
    public function testGetImportableTabs()
    {
        $this->assertInternalType('array', $this->object->getImportableTabs(0));
    }

    /**
     * Test
     *
     * @return void
     */
    public function testSetTabs()
    {
        $this->assertNull(
            $this->object->setTabs(
                array(
                    array(
                        'name' => 'TabTest',
                        'description' => 'TabTest',
                        'sort_order' => 2,
                        'document_type_id' => $this->documentType->getId(),
                    )
                )
            )
        );
    }

    /**
     * Test
     *
     * @return void
     */
    public function testAddTab()
    {
        $this->assertNull(
            $this->object->addTab(
                array(
                    'name' => 'TabTest',
                    'description' => 'TabTest',
                    'sort_order' => 2,
                    'document_type_id' => $this->documentType->getId(),
                )
            )
        );
    }

    /**
     * Test
     *
     * @return void
     */
    public function testSave()
    {
        $this->object->setTabs(
            array(
                array(
                    'name' => 'TabTest',
                    'description' => 'TabTest',
                    'sort_order' => 2,
                    'document_type_id' => $this->documentType->getId(),
                )
            )
        );

        $this->assertNull($this->object->save());
    }

    /**
     * Test
     *
     * @return void
     */
    public function testDelete()
    {
        $this->object->setTabs(
            array(
                array(
                    'name' => 'TabTest',
                    'description' => 'TabTest',
                    'sort_order' => 2,
                    'document_type_id' => $this->documentType->getId(),
                )
            )
        );
        $this->object->save();

        $this->assertTrue($this->object->delete());
    }
}
